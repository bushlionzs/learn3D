/*
 =======================================================================================================================
 =======================================================================================================================
 */
void CMapZone::AddRegion(int32 id)
{
	std::list<int32>::iterator	it;

	it = find(m_listRegion.begin(), m_listRegion.end(), id);

	if(it == m_listRegion.end())
	{
		m_listRegion.push_back(id);
	}
}

/*
 =======================================================================================================================
 =======================================================================================================================
 */
bool CMapZone::RemoveRegion(int32 id)
{
	std::list<int32>::iterator	it;

	it = find(m_listRegion.begin(), m_listRegion.end(), id);

	if(it != m_listRegion.end())
	{
		m_listRegion.erase(it);

		return true;
	}

	return false;
}
