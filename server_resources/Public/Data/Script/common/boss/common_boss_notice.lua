function x306001_OnDie(sceneId, selfId, killerId)
	local guid = GetMonsterGUID(sceneId, selfId)
	for i, item in x306001_g_Random_Pos do
		if item.sceneId == sceneId and item.GUID == guid then
			local r = random(1,3)
			if r < 1 then 
				r = 1 
			end
			if r > 3 then 
				r = 3
			end
			
			local pos = item.pos[r]
			
			SetRevPos(sceneId, selfId, pos.x, pos.z)
		end
	end
	
	-- 判断是否是宠物
	if GetObjType(sceneId, killerId) == 3 then
		killerId = GetMasterObjId(sceneId, killerId)
		if killerId == nil then
			return
		end
	end
	local Name = GetName(sceneId,killerId)
	local Name1 = GetName(sceneId,selfId)
	local scenename = GetSceneInfo(sceneId,"NAME")
	local msg1 = Name.."在"..scenename.."将"..Name1.."击败！"
	
	--LuaAllScenceM2Country( sceneId,msg1,0, 5,1)
	LuaAllScenceM2Wrold(sceneId, msg1, CHAT_LEFTDOWN, 0)
end



