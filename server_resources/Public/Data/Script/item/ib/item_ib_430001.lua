--**********************************
function x430001_OnConditionCheck( sceneId, selfId )
	--校验使用的物品
	
	if(1~=VerifyUsedItem(sceneId, selfId)) then
		return 0
	end
	if(IsNeedRepaireBodyItemDur( sceneId, selfId ) <= 0) then
		local str
		str = "没有装备需要修理"
		BeginQuestEvent( sceneId )
		AddQuestText( sceneId, str )
		EndQuestEvent()
		DispatchQuestTips( sceneId, selfId )

		return 0
	end
	return 1; --不需要任何条件，并且始终返回1。

end
