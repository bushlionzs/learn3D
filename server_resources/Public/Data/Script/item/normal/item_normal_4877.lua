--**********************************
--只会执行一次入口：
--聚气和瞬发技能会在消耗完成后调用这个接口（聚气结束并且各种条件都满足的时候），而引导
--技能也会在消耗完成后调用这个接口（技能的一开始，消耗成功执行之后）。
--返回1：处理成功；返回0：处理失败。
--注：这里是技能生效一次的入口
--**********************************
function x404877_OnActivateOnce( sceneId, selfId )
	local itemInBag = GetActuveItemTableIndex(sceneId, selfId)

	for i, item in x404877_g_MapList do
		if itemInBag == item.itemId then
			local retCode = x404877_IsCanSuccess(sceneId, selfId, item)
			if retCode ~= nil then
				BeginQuestEvent(sceneId);
				AddQuestText(sceneId, retCode);
				EndQuestEvent();
				DispatchQuestTips(sceneId, selfId);
				return 1
			end
			
			SetPrescription(sceneId, selfId, item.prescrId, 1)
			BeginQuestEvent(sceneId);
			AddQuestText(sceneId, "你学会了此配方");
			EndQuestEvent();
			DispatchQuestTips(sceneId, selfId);	
		end	
	end


	return 1;
end

