function x404877_IsCanSuccess(sceneId, selfId, checkTable)
		local level = QueryPlayerAbilityLevel( sceneId, selfId, checkTable.abilityId );
		if level <= 0 then
			return "您还没有学会相应的生活技能"
		end
		
		local needLevel = Prescription_NeedLevel(checkTable.prescrId)
		if level < needLevel then
			return "生活技能等级不够"
		end
		
		if IsPrescrLearned(sceneId, selfId, checkTable.prescrId) > 0 then
			return "此配方已经学过"
		end
		
		if checkTable.title ~= -1 and IsHaveTitle(sceneId, selfId, checkTable.title) <= 0 then
			return "此配方需要对应的称号才能学习"
		end
		
		return nil

end