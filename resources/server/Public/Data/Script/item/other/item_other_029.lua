--**********************************
--只会执行一次入口：
--聚气和瞬发技能会在消耗完成后调用这个接口（聚气结束并且各种条件都满足的时候），而引导
--技能也会在消耗完成后调用这个接口（技能的一开始，消耗成功执行之后）。
--返回1：处理成功；返回0：处理失败。
--注：这里是技能生效一次的入口
--**********************************
function x418029_OnActivateOnce( sceneId, selfId, impactId )
	
	if GetLevel(sceneId, selfId)< x418029_g_LevelMin then
		BeginQuestEvent(sceneId)
		AddQuestText(sceneId,"等级不足")
		EndQuestEvent(sceneId)
		DispatchQuestTips(sceneId,selfId)
		return
	end
	
	BeginAddItem(sceneId)
	AddBindItem( sceneId, 11050002, 5 )	--中级龙眼5
	AddBindItem( sceneId, 12030322, 1 )	--银卡30两
	AddBindItem( sceneId, 11000201, 5 )	--国传5个
	AddBindItem( sceneId, 11030501, 3 )	--熔火石3个
	AddBindItem( sceneId, 11030405, 3 )	--七步蛇3个
	AddBindItem( sceneId, 11020501, 3 )	--草本精华3个
	AddBindItem( sceneId, 11000101, 1 )	--生铁钻1个	
	AddBindItem( sceneId, 12010202, 1 )	--桂苓甘露散1个	

	local ret = EndAddItem(sceneId,selfId)
	if ret > 0 then
		if(DepletingUsedItem(sceneId, selfId)) == 1 then
			AddItemListToPlayer(sceneId,selfId)
			local nShengWang = GetShengWang( sceneId, selfId )
			SetShengWang( sceneId, selfId, nShengWang+5000 )--声望5000点
			BeginQuestEvent(sceneId)
			AddQuestText(sceneId,"打开礼包，获得了礼包道具！获得5000点声望！")
			EndQuestEvent(sceneId)
			DispatchQuestTips(sceneId,selfId)
		else
			BeginQuestEvent(sceneId)
			AddQuestText(sceneId,"使用物品异常！")
			EndQuestEvent(sceneId)
			DispatchQuestTips(sceneId,selfId)
		end
		
	else
		BeginQuestEvent(sceneId)
		AddQuestText(sceneId,"背包已满，无法得到物品！您需要至少有8个背包空间！")
		EndQuestEvent(sceneId)
		DispatchQuestTips(sceneId,selfId)
	end

end

