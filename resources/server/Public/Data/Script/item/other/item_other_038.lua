--**********************************
--只会执行一次入口：
--聚气和瞬发技能会在消耗完成后调用这个接口（聚气结束并且各种条件都满足的时候），而引导
--技能也会在消耗完成后调用这个接口（技能的一开始，消耗成功执行之后）。
--返回1：处理成功；返回0：处理失败。
--注：这里是技能生效一次的入口
--**********************************
function x418038_OnActivateOnce( sceneId, selfId, impactId )
	
	if GetLevel(sceneId, selfId)< x418038_g_LevelMin then
		BeginQuestEvent(sceneId)
		AddQuestText(sceneId,"等级不足")
		EndQuestEvent(sceneId)
		DispatchQuestTips(sceneId,selfId)
		return
	end
	
	BeginAddItem(sceneId)
	AddBindItem( sceneId, 12030321, 1 )	--银卡15两
	AddBindItem( sceneId, 11000200, 5 )	--世传5个
	AddBindItem( sceneId, 11050001, 5 )	--初级龙眼5 
	--AddBindItem( sceneId, 12030209, 1 )--江湖请柬1天？	--2009-9-16因外挂删除此道具
	AddBindItem( sceneId, 14040001, 1 )	--1级体质宝石1个
	AddBindItem( sceneId, 11000100, 1 )--赤铜钻1个
	AddBindItem( sceneId, 12010200, 3 )--天罡止血散3个
	AddBindItem( sceneId, 12050000, 1 )--常乐草1个

	local HorseNum = GetHorseCount(sceneId,selfId)
	local nLevel = GetLevel(sceneId,selfId)
	local tHNum = 0
	local settrue = 0

	if nLevel < 10 then 
		tHNum = 1
	elseif nLevel >= 10 and nLevel < 20 then
		tHNum = 2
	elseif nLevel >= 20 and nLevel < 30 then
		tHNum = 3
	elseif nLevel >= 30 and nLevel < 40 then
		tHNum = 4
	elseif nLevel >= 40 and nLevel < 50 then
		tHNum = 5
	elseif nLevel >= 50 then
		tHNum = 6
	end

	if tHNum > HorseNum then
		settrue = 1
	else
		settrue = 0
	end

	local ret = EndAddItem(sceneId,selfId)
	if ret > 0 and settrue > 0 then
		
		local a = CreateHorseToPlayer(sceneId,selfId,3630,-1)
		
		if(DepletingUsedItem(sceneId, selfId)) == 1 then
			BeginQuestEvent(sceneId)
			AddQuestText(sceneId,"打开礼包，获得了礼包道具！")
			AddQuestText(sceneId,"恭喜您获得喜鹊坐骑！")
			EndQuestEvent(sceneId)
			DispatchQuestTips(sceneId,selfId)
			AddItemListToPlayer(sceneId,selfId)
		else
			BeginQuestEvent(sceneId)
			AddQuestText(sceneId,"使用物品异常！")
			EndQuestEvent(sceneId)
			DispatchQuestTips(sceneId,selfId)
		end
		
	else
		BeginQuestEvent(sceneId)
		AddQuestText(sceneId,"背包或者坐骑已满，无法得到物品！您需要至少有7个背包空间和1个坐骑空间！")	--2009-9-16删除一个道具因此减一格
		EndQuestEvent(sceneId)
		DispatchQuestTips(sceneId,selfId)
	end

end

