		if s.metier == cur_metier then
			for j , u in s.list do
			
				local num = u.num;
				local id = u.id;
				
				if ( id ~= nil ) then
					if( j < getn( s.list ) ) then
						AddQuestItemBonus( sceneId, id, num);
					else
						if ( extid == getn( x418156_item_array ) ) then
							AddQuestItemBonus( sceneId, id, num);
						end
					end
				else
					--随机显示
					local random_key = x418156_get_random_key( lv );
					
					if ( random_key ~= nil ) then
					
						local copy = {};
						local ref = x418156_item_random_list[ random_key ];
						
						for x = 1 , x418156_array_size do
							copy[ x ] = ref[ x ];
						end
						
						local copy_l = getn( copy );
					
						for k = 1 , num do
							for i = 1 , copy_l do
								local r = random( 1 , copy_l );
								local r_id = copy[ r ];
								
								if r_id ~= -1 then
									copy[ r ] = -1;
									AddQuestItemBonus( sceneId , r_id , 1 );
									break
								end
							end
						end
						
					end
				end
			end	
		end
	end

	SetPlayerRuntimeData( sceneId, selfId, RD_ROOKIE_BOX, extid)
	EndQuestEvent();
	DispatchQuestInfoNM( sceneId, selfId, targetId,418156,10263);
end



