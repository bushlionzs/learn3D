end


function x418156_ProcQuestAccept( sceneId, selfId, targetId, missionId )

	local extid = GetPlayerRuntimeData( sceneId, selfId, RD_ROOKIE_BOX);
	local tb_id = x418156_get_item_key( sceneId , selfId );
	
	if ( tb_id == nil ) then
		return
	end
	
	if ( extid == nil ) or 
	   ( extid > x418156_array_size ) then
	   return
	end
	
	if ( tb_id > extid ) then
		Msg2Player( sceneId, selfId, "您已经领取过该级别礼品", 8, 3 )
		return
	end
	
	if ( tb_id < extid ) then
		local key_lv = x418156_item_list[ tb_id ].lv;
		Msg2Player( sceneId, selfId, "您需要先领取"..tostring( key_lv ).."级的礼包", 8, 3 );
		return
	end

	local lv = x418156_item_list[ extid ].lv;
	local cur_lv =GetLevel( sceneId , selfId );
	local cur_metier = GetZhiye( sceneId , selfId );
	
	--判断级别
	if cur_lv < lv then
		Msg2Player( sceneId, selfId, "您还没有达到该级别", 8, 3 )
		return
	end
	
	--判断是否该包
	
	
	BeginAddItem(sceneId);
	
	local list = x418156_item_list[ extid ].array;
	local get_num = 0;
	
	for i , s in list do
		--职业匹配
		if s.metier == cur_metier then
		
			for j , u in s.list do
				
				--赋予道具
				local num = u.num;
				local id = u.id;
				
				if id == nil then

					--随机道具
					local random_key = x418156_get_random_key( lv );
					
					if ( random_key ~= nil ) then
					
						local copy = {};
						local ref = x418156_item_random_list[ random_key ];
						
						for x = 1 , x418156_array_size do
							copy[ x ] = ref[ x ];
						end

						local copy_l = getn( copy );
					
						for k = 1 , num do
							for i = 1 , copy_l do
								local r = random( 1 , copy_l );
								local r_id = copy[ r ];
								
								if r_id ~= nil then
									copy[ r ] = nil;
									AddBindItem( sceneId , r_id , 1 );
									get_num = get_num + 1;
									break
								end
							end
						end
						
					end

					
				else
					--不随机
					AddBindItem( sceneId , id , num );
					get_num = get_num + 1;
				end
				
			end
			
		end
	end
	
	
	local ret = EndAddItem(sceneId,selfId);
	
	if get_num == 0 then
		x418156_output_msg( sceneId,selfId,"获取道具失败！" );
	end
	
	local gold = x418156_gift_gold_array[ extid ];	
	
	if ( ret > 0 ) then
	
		if(DepletingUsedItem(sceneId, selfId)) == 1 then
			if ( gold ~= 0 ) and ( gold ~= nil ) then
				x418156_output_msg( sceneId,selfId,"打开礼包，获得了礼包道具、金卡"..tostring( gold ).."两" );
	
				if ( gold ~= 0 ) and ( gold ~= nil ) then
					local money = gold * 1000;
					AddMoney(sceneId,selfId,3,money,418156);
				end
			else
				x418156_output_msg( sceneId,selfId,"打开礼包，获得了礼包道具" );
			end
			
			AddItemListToPlayer(sceneId,selfId);
		else
			x418156_output_msg( sceneId,selfId,"使用物品异常" );
		end
	else
		x418156_output_msg( sceneId,selfId,"背包已满，无法得到物品！您需要至少有"..tostring( get_num ).."个背包空间！" );
	end
	



	
	
end

